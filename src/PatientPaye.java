public class PatientPaye {		
	
	
	private int montant;
	private int facture;
	private int prisencharge;
	
	
	
	public int getMontant() {
		return montant;
	}

	public void setMontant(int montant) {
		this.montant = montant;
	}

	public int getFacture() {
		return facture;
	}

	public void setFacture(int facture) {
		this.facture = facture;
	}

	public int getPrisencharge() {
		return prisencharge;
	}

	public void setPrisencharge(int prisencharge) {
		this.prisencharge = prisencharge;
	}
}
	
